function cekRelasi(a, b) {
    console.log(a + " == " + b + " : " + (a == b));
    console.log(a + " === " + b + " : " + (a === b));
    console.log(a + " != " + b + " : " + (a != b));
    console.log(a + " !== " + b + " : " + (a !== b));
    console.log(a + " > " + b + " : " + (a > b));
    console.log(a + " < " + b + " : " + (a < b));
    console.log(a + " >= " + b + " : " + (a >= b));
    console.log(a + " <= " + b + " : " + (a <= b));
}

cekRelasi(5, "5");