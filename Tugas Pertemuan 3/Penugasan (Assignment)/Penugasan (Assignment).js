function contohAssignment() {
    let x = 5;
    console.log("Nilai awal x: " + x);

    x += 3;  // x = x + 3
    console.log("Setelah x += 3 : " + x);

    x -= 2;  // x = x - 2
    console.log("Setelah x -= 2 : " + x);

    x *= 4;  // x = x * 4
    console.log("Setelah x *= 4 : " + x);

    x /= 2;  // x = x / 2
    console.log("Setelah x /= 2 : " + x);

    x %= 3;  // x = x % 3
    console.log("Setelah x %= 3 : " + x);
}

contohAssignment();